function [t,S,V] = trajectory_simulation(S0,v0,r,kappa,theta,sigma,rho)
  
    % Create Heston model
    Heston = heston( ...
        'StartState', [S0 v0], ...
        'Return', r, ...
        'Kappa', kappa, ...
        'Theta', theta, ...
        'Sigma', sigma, ...
        'Rho', rho);
    
    % Time setup
    dt = 1/252;
    T = 1;
    N = T/dt;
    nPaths = 1000;
    
    % Simulate paths
    [Paths, t] = simulate(Heston, N, ...
        'DeltaTime', dt, ...
        'nTrials', nPaths);
    
    S = Paths(:,:,1); % asset paths
    V = Paths(:,:,2); % variance paths
end